    private String getCurrentDate(){
        Calendar cal = new GregorianCalendar();
        int month = cal.get(Calendar.MONTH);
        int year = cal.get(Calendar.YEAR);
        int day = cal.get(Calendar.DAY_OF_MONTH);
        lbl_date.setText("Date: " + year + "/" + (month+1) + "/" + day); 
        String date = year + "/" + (month+1) + "/" + day;
                
        return date;
    }
    private void getData(String id){
        int itr = 1;
        try{
            String sql = "select * from Exam natural join course where `Student #`= ?";
            pst = connection.prepareStatement(sql);
            pst.setString(1, id);
            rs = pst.executeQuery();
            
            while(rs.next()){
                if(itr == 1){
                    lbl_database.setText(rs.getString("course code"));
                    lbl_database_score.setText(rs.getString("score"));
                    itr++;
                }
                else if(itr == 2){
                    lbl_java.setText(rs.getString("course code"));
                    lbl_java_score.setText(rs.getString("score"));
                    itr++;
                }
                else if(itr == 3){
                    lbl_sql.setText(rs.getString("course code"));
                    lbl_sql_score.setText(rs.getString("score"));
                    itr++;
                }
            }
            pst.close();
            rs.close();
            }catch(Exception e){
                e.printStackTrace();
            }
    }
    
    private void getTotal(String id){
        String sql = "select sum(score) from Exam where `Student #`= ?";
        try{
            pst = connection.prepareStatement(sql);
            pst.setString(1, id);
            rs = pst.executeQuery();
            
            while(rs.next()){
                
                lbl_score.setText(rs.getString("sum(score)"));
                
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    
    
