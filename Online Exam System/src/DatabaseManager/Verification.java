    public void showStudentJTable(){
        ArrayList<Student> list = getData();
        DefaultTableModel model = (DefaultTableModel)table_studentList.getModel();
        
        Object[] rowData = new Object[7];
        for(int i = 0; i < getData().size(); i++){
            
            rowData[0] = list.get(i).getStudentNumber();
            rowData[1] = list.get(i).getUsername();
            rowData[2] = list.get(i).getPassword();
            rowData[3] = list.get(i).getFirstName();
            rowData[4] = list.get(i).getLastName();
            rowData[5] = list.get(i).getEmail();
            rowData[6] = list.get(i).getProgram();
        						
        model.addRow(rowData);
        }
    }
    
