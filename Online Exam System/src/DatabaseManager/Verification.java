    private void txt_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyReleased
        // TODO add your handling code here:
        String sql = "select * from Student where `Student #`=?";
        try{
            
            pst = connection.prepareStatement(sql);
            pst.setString(1, txt_search.getText());
            rs = pst.executeQuery();
            
            if(rs.next()){
                String value1 = rs.getString("student #");
                lbl_student_number.setText(value1);
                String value2 = rs.getString("username");
                txt_username.setText(value2);
                String value3 = rs.getString("password");
                txt_password.setText(value3);
                String value4 = rs.getString("first name");
                txt_first_name.setText(value4);
                String value5 = rs.getString("last name");
                txt_last_name.setText(value5);
                String value6 = rs.getString("email");
                txt_email.setText(value6);
                String value7 = rs.getString("program");              
                comboBox_program.setSelectedItem(value7);
            }
            
            pst.close();
            rs.close();
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_txt_searchKeyReleased
 
