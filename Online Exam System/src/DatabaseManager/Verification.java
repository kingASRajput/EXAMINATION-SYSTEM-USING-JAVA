    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        String value1 = txt_username.getText();
        String value2 = txt_password.getText();
        String value3 = txt_first_name.getText();
        String value4 = txt_last_name.getText();
        String value5 = lbl_student_number.getText();
        String value6 = txt_email.getText();
        String value7 = comboBox_program.getSelectedItem().toString();
        
        if(isFieldEmpty()){
            JOptionPane.showMessageDialog(null, "Please fill up required fields!");
        }
        else{
            String sql ="update Student set username='"+value1+"'"
                   + " ,password='"+value2+"'"
                   + " ,`first name`=upper('"+value3+"')"
                   + " ,`last name`=upper('"+value4+"')"
                   + " ,email='"+value6+"'"
                   + " ,program='"+value7+"'"
                   + " where `student #`='"+value5+"' ";
            try {
                pst = connection.prepareStatement(sql);
                pst.execute();
                pst.close();
                JOptionPane.showMessageDialog(null, "Data Updated");
                
            } catch (SQLException ex) {
                Logger.getLogger(Verification.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
            Update_table();
            clear();
        }
    }//GEN-LAST:event_btn_updateActionPerformed

