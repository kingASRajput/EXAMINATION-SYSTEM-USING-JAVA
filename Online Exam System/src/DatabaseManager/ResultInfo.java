    public void byAverage(){
        try{
            String sql = "select truncate(avg(theSum)/count(cBSIT), 0) "
                    + "from (select sum(score) as theSum, program as cBSIT from result natural join student group by program having program in ('BSIT') ) "
                    + "result";
            pst = connection.prepareStatement(sql);
            rs = pst.executeQuery();
            table_student_info.setModel(DbUtils.resultSetToTableModel(rs));
            
            pst.close();
            rs.close();
        }catch(Exception e){
        }
    }
