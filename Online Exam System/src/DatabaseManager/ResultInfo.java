     private void show_Exam_Table(){
        try{
            String sql = "select Date, `Student #`, concat(`last name`, ', ', `first name`) as Name, `Course Code`, Score "
                    + "from Exam natural join Student";
            pst = connection.prepareStatement(sql);
            rs = pst.executeQuery();
            table_student_info.setModel(DbUtils.resultSetToTableModel(rs));
            
            pst.close();
        }catch(Exception e){
        }
    }
     
    public void byStudent(){
        try{
            String sql = "select concat(`last name`, ', ', `first name`) as Name, Program, sum(score) as `Total Score` "
                    + "from Exam natural join Student group by `student #` "
                    + "order by sum(score) desc";
            pst = connection.prepareStatement(sql);
            rs = pst.executeQuery();
            table_student_info.setModel(DbUtils.resultSetToTableModel(rs));
            
            pst.close();
            rs.close();
        }catch(Exception e){
        }
    }
     
    public void byProgram(){
        try{
            String sql = "select program, count(distinct(`student #`)) as 'Total Examinees' , "
                    + "sum(score) as 'Total Score', "
                    + "truncate(sum(score)/count(distinct(`student #`)), 0) as 'Average Score' "
                    + "from Exam natural join Student "
                    + "group by program "
                    + "order by sum(score) desc";
            pst = connection.prepareStatement(sql);
            rs = pst.executeQuery();
            table_student_info.setModel(DbUtils.resultSetToTableModel(rs));
            
            pst.close();
            rs.close();
        }catch(Exception e){
        }
    } 
    
