    private void btn_sign_inActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sign_inActionPerformed
        // TODO add your handling code here:
        if(isUserExists()){
            JOptionPane.showMessageDialog(null, "You already done!");
            clear();
        }
        else{
            try{
                String sql = "select * from student where username=? and password=?";
                pst=connection.prepareStatement(sql);
                pst.setString(1, txt_username.getText());
                pst.setString(2, txt_password.getText());
                rs = pst.executeQuery();

                if(rs.next()){
                     if(rs.getString("password").equals(txt_password.getText())){
                        JOptionPane.showMessageDialog(null, "Username and Password is Correct");
                        MainFrame mainframe = new MainFrame(txt_username.getText());
                        SwingUtilities.getWindowAncestor(btn_sign_in).dispose();
                        mainframe.setVisible(true);
                     }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Invalid input!");
                }
                rs.close();
                pst.close();
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_sign_inActionPerformed

