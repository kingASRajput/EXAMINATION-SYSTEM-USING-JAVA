    public boolean isUserExists(){
       String username = txt_username.getText();
       
       try{
           String sql = "select `student #` from "
                   + "(select * from exam)t "
                   + "natural join Student where username = ?";
           pst = connection.prepareStatement(sql);
           pst.setString(1, username);
           rs = pst.executeQuery();
           
           if(rs.next()){
               return true;
           }
       
       }catch(Exception e){
           e.printStackTrace();
       }
       return false;
   }
